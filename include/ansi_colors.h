#ifndef ANSI_COLORS_H_
    #define ANSI_COLORS_H_

    // ANSI escape codes for colors
    #define COLOR_RESET   "\033[0m"

    #define COLOR_IDENTIFIER    "\033[38;5;140m"
    #define COLOR_SYMBOL        "\033[38;5;251m"
    #define COLOR_NUMBER        "\033[38;5;208m"
    #define COLOR_STRING        "\033[38;5;106m"
    #define COLOR_SPACE         "\033[38;5;240m"
    #define COLOR_TAB           "\033[38;5;240m"
    #define COLOR_NEWLINE       "\033[38;5;240m"
    #define COLOR_COMMENT_BIG   "\033[38;5;102m"
    #define COLOR_COMMENT_SMALL "\033[38;5;102m"
    #define COLOR_PREPROCESSOR  "\033[38;5;67m"
    #define COLOR_PARENT_OPEN   "\033[38;5;166m"
    #define COLOR_PARENT_CLOSE  "\033[38;5;166m"
    #define COLOR_BRACKET_OPEN  "\033[38;5;209m"
    #define COLOR_BRACKET_CLOSE "\033[38;5;166m"
    #define COLOR_SUPERIOR      "\033[38;5;67m"
    #define COLOR_INFERIOR      "\033[38;5;67m"
    #define COLOR_EQUAL         "\033[38;5;67m"
    #define COLOR_SEMICOLON     "\033[38;5;167m"
    #define COLOR_SQUARE_BR_OPEN  "\033[38;5;166m"
    #define COLOR_SQUARE_BR_CLOSE "\033[38;5;166m"
    #define COLOR_COMMA         "\033[38;5;167m"
    #define COLOR_STAR          "\033[38;5;67m"
    #define COLOR_ADD           "\033[38;5;67m"
    #define COLOR_DIV           "\033[38;5;67m"
    #define COLOR_SUB           "\033[38;5;67m"
    #define COLOR_CHAR          "\033[38;5;196m"
    #define COLOR_AND           "\033[38;5;67m"
    #define COLOR_OR            "\033[38;5;67m"
    #define COLOR_FUNC          "\033[38;5;67m"
    #define COLOR_END           "\033[0m"

#endif /* ANSI_COLORS_H_ */
